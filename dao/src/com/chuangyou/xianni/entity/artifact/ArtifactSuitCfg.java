package com.chuangyou.xianni.entity.artifact;

public class ArtifactSuitCfg {

	/** id */
	private int id;
	
	/** 激活件数 */
	private int suitid;
	
	/** 增加所有属性万分比 */
	private int att;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSuitid() {
		return suitid;
	}

	public void setSuitid(int suitid) {
		this.suitid = suitid;
	}

	public int getAtt() {
		return att;
	}

	public void setAtt(int att) {
		this.att = att;
	}
}

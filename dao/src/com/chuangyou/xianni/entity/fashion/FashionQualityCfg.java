package com.chuangyou.xianni.entity.fashion;

public class FashionQualityCfg {
	private int quality;
	private String qualityName;
	private int qualityUpConsume;
	private int levelUpConsume;
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public String getQualityName() {
		return qualityName;
	}
	public void setQualityName(String qualityName) {
		this.qualityName = qualityName;
	}
	public int getQualityUpConsume() {
		return qualityUpConsume;
	}
	public void setQualityUpConsume(int qualityUpConsume) {
		this.qualityUpConsume = qualityUpConsume;
	}
	public int getLevelUpConsume() {
		return levelUpConsume;
	}
	public void setLevelUpConsume(int levelUpConsume) {
		this.levelUpConsume = levelUpConsume;
	}
}

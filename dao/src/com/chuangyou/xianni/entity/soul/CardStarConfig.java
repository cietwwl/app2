package com.chuangyou.xianni.entity.soul;

public class CardStarConfig {
	private int star;
	private int spendNum;
	private int attrAdd;
	public int getStar() {
		return star;
	}
	public void setStar(int star) {
		this.star = star;
	}
	
	public int getAttrAdd() {
		return attrAdd;
	}
	public void setAttrAdd(int attrAdd) {
		this.attrAdd = attrAdd;
	}
	public int getSpendNum() {
		return spendNum;
	}
	public void setSpendNum(int spendNum) {
		this.spendNum = spendNum;
	}
	
	
	
}

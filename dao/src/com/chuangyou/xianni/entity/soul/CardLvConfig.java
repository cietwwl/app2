package com.chuangyou.xianni.entity.soul;

public class CardLvConfig {
	private int lev;
	private int exp;
	private int spendItem;
	private int addExp;
	
	
	public int getLev() {
		return lev;
	}
	public void setLev(int lev) {
		this.lev = lev;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getSpendItem() {
		return spendItem;
	}
	public void setSpendItem(int spendItem) {
		this.spendItem = spendItem;
	}
	public int getAddExp() {
		return addExp;
	}
	public void setAddExp(int addExp) {
		this.addExp = addExp;
	}
	
}

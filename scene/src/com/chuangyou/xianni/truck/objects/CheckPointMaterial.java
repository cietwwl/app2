package com.chuangyou.xianni.truck.objects;

/**
 * 检查点的物质
 * @author wkghost
 *
 */
public class CheckPointMaterial {
	private int id;
	private int remain;
	private int price;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRemain() {
		return remain;
	}
	public void setRemain(int remain) {
		this.remain = remain;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
}
